function showProduct(product) {
    let x = ``;
    let title = ``;
    var urlID = window.location.href;
    res1 = urlID.slice(-1);
    $.each(product, function (k, v) {
        switch (res1) {
            case "1":
                title = `Hoa Giả Trang Trí `;
                break;
            case "2":
                title = `Hoa Tươi Trang Trí `;
                break;
            case "3":
                title = `Chậu Hoa Trang Trí `;
                break;
            case "4":
                title = `Giỏ Hoa Trang Trí `;
                break;
        }
        if (res1 == v.type) {
            x += `
                  <div class="col-3">
                      <a href="detail.html?name=${v.id}">
                          <div class="thumbnail">
                              <img src="${v.pic}" alt="Hoa" class="img-thumbnail">
                              <div class="card-body">
                                  <h3 style="font-size: 1.8rem;">${v.name}</h3>
                                  <p>${v.price}.000₫</p>
                                  <input type="button" class="btn btn-outline-danger" value="Xem thông tin"></input>
                              </div>
                          </div>
                      </a>
                  </div>`;
        }
    });
    $("#title").html(title);
    $("#show-product").html(x);
}

